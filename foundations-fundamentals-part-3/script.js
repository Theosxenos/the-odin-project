function add7(number)
{
    return number + 7;
}

function multiply(x,y)
{
    return x * y;
}

function capitalize(string)
{
    string = string[0].toUpperCase() + string.substring(1).toLowerCase();
    
    return string;
}

function lastLetter(string)
{
    return string.substring(string.length - 1);
}